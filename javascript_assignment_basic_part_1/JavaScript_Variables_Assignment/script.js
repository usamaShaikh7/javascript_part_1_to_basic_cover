// Declare a string variable
let myName = "Usama"; // This variable stores a string value

// Declare a number variable
let age = 25; // This variable stores a number

// Declare a boolean variable
let isStudent = true; // This variable stores a boolean value

// Log the values and their types to the console
console.log("Value of name:", myName); // Output the value of 'name'
console.log("Type of name:", typeof myName ); // Output the type of 'name' (string)

console.log("Value of age:", age); // Output the value of 'age'
console.log("Type of age:", typeof age); // Output the type of 'age' (number)

console.log("Value of isStudent:", isStudent); // Output the value of 'isStudent'
console.log("Type of isStudent:", typeof isStudent); // Output the type of 'isStudent' (boolean)
