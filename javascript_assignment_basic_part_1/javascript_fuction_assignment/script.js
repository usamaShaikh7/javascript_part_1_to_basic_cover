function introduce(name, age) {
    return `Hello, my name is ${name} and I am ${age} years old.`;
}

// Test the function with different values
console.log(introduce("usama", 20));  // Output: Hello, my name is Alice and I am 30 years old.
console.log(introduce("roshan", 25));    // Output: Hello, my name is Bob and I am 25 years old.
console.log(introduce("anjali", 22)); // Output: Hello, my name is Charlie and I am 22 years old.
console.log(introduce("xyz", 45));   // Output: Hello, my name is Diana and I am 45 years old.
