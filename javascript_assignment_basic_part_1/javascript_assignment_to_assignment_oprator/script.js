// Initial value
let value = 10;
console.log("Initial value:", value);

// Assignment operator (=)
value = 20; // Assigns 20 to value
console.log("After assignment (=):", value);

// Addition assignment operator (+=)
value += 5; // Adds 5 to value (20 + 5)
console.log("After addition assignment (+=):", value);

// Subtraction assignment operator (-=)
value -= 3; // Subtracts 3 from value (25 - 3)
console.log("After subtraction assignment (-=):", value);

// Multiplication assignment operator (*=)
value *= 2; // Multiplies value by 2 (22 * 2)
console.log("After multiplication assignment (*=):", value);

// Division assignment operator (/=)
value /= 4; // Divides value by 4 (44 / 4)
console.log("After division assignment (/=):", value);
